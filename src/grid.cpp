// 将 RAW_MAP 转为 vector
std::vector<std::vector<int>> loadHandWrittenMap(int choice)
{
    std::vector<std::vector<int>> grid(40, std::vector<int>(40));
    for (int y = 0; y < 40; ++y)
        for (int x = 0; x < 40; ++x)
            if(choice == 1)
                grid[y][x] = RAW_MAP1[y][x];
            else if (choice == 2)
                grid[y][x] = RAW_MAP2[y][x];
            else if (choice == 3)
                grid[y][x] = RAW_MAP3[y][x];
    return grid;
}
